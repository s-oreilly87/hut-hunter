    async def _get_site_list_table(self, page: Page) -> Locator:
        """Locate the main Site List table.

        Some DOC facilities render summary rows like "Available # of People" /
        "Number of sites available"; others render only per-unit rows. Anchor
        on the "Site List" section first, then fall back to any visible table
        whose first header looks like a site-name column.
        """
        site_list_heading = page.get_by_text(
            re.compile(r"^Site List$", re.IGNORECASE)
        ).first
        try:
            await site_list_heading.wait_for(state="visible", timeout=20_000)
            for depth in range(1, 7):
                container = site_list_heading.locator(f"xpath=ancestor::div[{depth}]")
                table = container.locator("table").first
                if await table.count() > 0:
                    await table.wait_for(state="visible", timeout=5_000)
                    return table
        except PlaywrightTimeoutError:
            pass

        table = page.locator("table").filter(
            has_text=re.compile(r"\bSite\b", re.IGNORECASE)
        ).first
        await table.wait_for(state="visible", timeout=20_000)
        return table

    async def _wait_for_table_change(self, page: Page, timeout_ms: int = 20_000) -> None:
        """Wait for the Site List table to refresh after selecting dates."""
        siteListHeading = page.get_by_text(
            re.compile(r"^Site List$", re.IGNORECASE)
        ).first
        try:
            await siteListHeading.wait_for(state="visible", timeout=20_000)
            container = siteListHeading.locator(
                "xpath=ancestor::div[contains(., 'Maximum Capacity')][1]"
            ).first
        except PlaywrightTimeoutError:
            # No "Site List" heading — fall back to the table itself.
            container = await self._get_site_list_table(page)

        try:
            before = _normalize(await container.inner_text())
        except Exception:
            return

        elapsed = 0
        while elapsed < timeout_ms:
            try:
                last = _normalize(await container.inner_text())
            except Exception:
                last = before
            if last != before:
                await page.wait_for_timeout(1200)
                return
            await page.wait_for_timeout(300)
            elapsed += 300

    async def _extract_column_data(
        self, page: Page, hut_name: str, target_day: int, target_month: str
    ) -> dict:
        """Parse the Site List table and pull the cells for ``target_day``.

        Returns a dict with ``ok=True`` plus either summary-row cell data
        (hut-style / campsite-summary pages) or ``unit_cells`` for per-unit
        campsite grids. Returns ``ok=False`` + ``reason`` otherwise.

        THR-128: *target_month* is a defense-in-depth guard, independent of
        the fast-path decision in ``_target_day_in_visible_table``. DOC
        renders a month label (e.g. "July") above/alongside the day-number
        headers; if that label is visible anywhere in the table and names a
        month other than *target_month*, we're reading the wrong month's
        columns entirely (day-of-month numbers repeat every month, so a
        column match on day alone proves nothing). Fail loudly with a
        distinct evidence message instead of silently reporting an
        availability result computed against the wrong month.
        """
        table = await self._get_site_list_table(page)
        rows = table.locator("tr")
        row_count = await rows.count()

        target_dd = str(target_day).zfill(2)
        col_idx = -1
        seen_rows: list[list[str]] = []

        for i in range(row_count):
            cells = rows.nth(i).locator("th, td")
            cell_count = await cells.count()
            texts: list[str] = []
            for j in range(cell_count):
                try:
                    raw = await cells.nth(j).inner_text()
                except Exception:
                    raw = ""
                txt = _normalize(raw)
                texts.append(txt)

                # Recognise the day-header column. DOC renders this cell as
                # "06", "6", "F 06", "Fri 06", etc. — accept any of them.
                if col_idx == -1 and (
                    txt == target_dd
                    or txt == str(target_day)
                    or re.search(rf"\b{target_dd}$", txt)
                ):
                    col_idx = j
            seen_rows.append(texts)

        if col_idx == -1:
            return {
                "ok": False,
                "reason": f"Could not find target day column {target_dd}",
                "seen_rows": seen_rows[:12],
            }

        # THR-128 defense-in-depth: a day-column match is meaningless if the
        # table is actually showing a different month. Scan every cell we
        # already collected for something that looks like a standalone
        # month label and make sure none of them contradict target_month.
        month_labels_seen = [
            txt
            for row in seen_rows
            for txt in row
            if _MONTH_LABEL_RE.match(txt)
        ]
        mismatched_labels = [
            txt for txt in month_labels_seen if _text_contradicts_month(txt, target_month)
        ]
        if mismatched_labels:
            return {
                "ok": False,
                "reason": (
                    f"table is showing {mismatched_labels[0]!r} — expected "
                    f"{target_month!r} (THR-128 month-mismatch guard)"
                ),
                "seen_rows": seen_rows[:12],
            }

        site_row_idx = -1
        people_row_idx = -1
        sites_row_idx = -1
        unit_row_indices: list[int] = []

        for i, row_cells in enumerate(seen_rows):
            first = row_cells[0] if row_cells else ""
            row_len = len(row_cells)
            if site_row_idx == -1 and hut_name in first and row_len > col_idx:
                site_row_idx = i
            if (
                people_row_idx == -1
                and re.search(r"Available # of People", first, re.IGNORECASE)
                and row_len > col_idx
            ):
                people_row_idx = i
            if (
                sites_row_idx == -1
                and re.search(r"Number of sites available", first, re.IGNORECASE)
                and row_len > col_idx
            ):
                sites_row_idx = i
            if (
                row_len > col_idx
                and first
                and not re.search(
                    r"^(Site|Available # of People|Number of sites available)$",
                    first,
                    re.IGNORECASE,
                )
            ):
                unit_row_indices.append(i)

        async def _read_cell(row_idx: int) -> dict:
            if row_idx < 0 or col_idx < 0:
                return {"text": "", "html": ""}
            cell = rows.nth(row_idx).locator("th, td").nth(col_idx)
            try:
                text = _normalize(await cell.inner_text())
            except Exception:
                text = ""
            try:
                html = await cell.inner_html()
            except Exception:
                html = ""
            return {"text": text, "html": html}

        site_cell = await _read_cell(site_row_idx)
        people_cell = await _read_cell(people_row_idx)
        sites_cell = await _read_cell(sites_row_idx)
        unit_cells: list[dict] = []
        for row_idx in unit_row_indices:
            row_cells = rows.nth(row_idx).locator("th, td")
            try:
                site_name = _normalize(await row_cells.nth(0).inner_text())
            except Exception:
                site_name = ""
            if not site_name or site_name == hut_name:
                continue
            cell = await _read_cell(row_idx)
            unit_cells.append(
                {
                    "site_name": site_name,
                    "text": cell["text"],
                    "html": cell["html"],
                }
            )

        return {
            "ok": True,
            "col_idx": col_idx,
            "site_cell_text": site_cell["text"],
            "site_cell_html": site_cell["html"],
            "people_cell_text": people_cell["text"],
            "sites_cell_text": sites_cell["text"],
            "unit_cells": unit_cells,
            "debug": {
                "site_row_idx": site_row_idx,
                "people_row_idx": people_row_idx,
                "sites_row_idx": sites_row_idx,
                "unit_row_count": len(unit_cells),
            },
        }

    def _classify(
        self, parsed: dict, hut_name: str, people_wanted: int
    ) -> AvailabilityResult:
        """Turn parsed cell text/html into an AvailabilityResult.

        Mirrors the heuristic in ``detectMuellerAvailability`` in watch.ts
        but uses the three-state status enum (AVAILABLE / PARTIALLY_AVAILABLE
        / UNAVAILABLE) introduced by the Great Walk adapter so downstream
        notification logic can treat both adapters uniformly.
        """
        if not parsed.get("ok"):
            return AvailabilityResult(
                site=hut_name,
                status=AvailabilityStatus.UNKNOWN,
                evidence=parsed.get("reason", "Could not parse Site List table"),
            )

        people_text = (parsed.get("people_cell_text") or "").strip()
        sites_text = (parsed.get("sites_cell_text") or "").strip()
        site_cell_text = (parsed.get("site_cell_text") or "").strip()
        site_cell_html = parsed.get("site_cell_html") or ""

        people_num = int(people_text) if re.fullmatch(r"\d+", people_text) else None
        sites_num = int(sites_text) if re.fullmatch(r"\d+", sites_text) else None

        unit_cells = parsed.get("unit_cells") or []
        cell_is_bookable, aria_status, icon = _parse_site_cell_status(site_cell_html)

        logger.debug(
            "_classify: hut=%r people=%r sites=%r icon=%r bookable=%s aria_status=%r",
            hut_name, people_text, sites_text, icon, cell_is_bookable, aria_status,
        )

        # Some campsite pages omit the summary rows entirely and only render
        # one unit cell per site. In that mode the top-level site cell is blank,
        # so we must classify from the unit grid before applying the summary
        # cell heuristics.
        if unit_cells and not site_cell_html.strip():
            available_units = 0
            sample_icon = icon
            sample_statuses: list[str] = []
            for unit in unit_cells:
                unit_html = unit.get("html") or ""
                unit_bookable, unit_status, unit_icon = _parse_site_cell_status(unit_html)
                if unit_bookable:
                    available_units += 1
                if unit_status:
                    sample_statuses.append(f"{unit.get('site_name')}: {unit_status}")
                if sample_icon is None and unit_icon is not None:
                    sample_icon = unit_icon

            avail_status = (
                AvailabilityStatus.AVAILABLE
                if available_units > 0
                else AvailabilityStatus.UNAVAILABLE
            )
            return AvailabilityResult(
                site=hut_name,
                status=avail_status,
                evidence=(
                    f"unitCells available={available_units}/{len(unit_cells)} "
                    f"sampleStatuses={sample_statuses[:4]!r}"
                ),
                total_available=available_units,
                icon=sample_icon,
            )

        if not cell_is_bookable or re.search(r"booked|closed|unavailable|not.available", aria_status):
            return AvailabilityResult(
                site=hut_name,
                status=AvailabilityStatus.UNAVAILABLE,
                evidence=(
                    f"Site cell not bookable online. "
                    f"tabindex0={cell_is_bookable} aria_status={aria_status!r} "
                    f"icon={icon!r}"
                ),
                total_available=0,
                icon=icon,
            )

        # ── Cell is clickable → determine capacity ────────────────────────────
        # DOC facilities use one of two rows to show how many spots remain:
        #   • "Available # of People"  — hut-style (beds/bunks)
        #   • "Number of sites available" — campsite-style
        # Prefer people count; fall back to site count. Either > 0 confirms
        # the date is genuinely bookable.

        count = people_num if people_num is not None else sites_num
        count_source = "people" if people_num is not None else "sites"

        if count is not None:
            if count_source == "sites":
                avail_status = (
                    AvailabilityStatus.AVAILABLE
                    if count > 0
                    else AvailabilityStatus.UNAVAILABLE
                )
            elif count >= people_wanted:
                avail_status = AvailabilityStatus.AVAILABLE
            elif count > 0:
                avail_status = AvailabilityStatus.PARTIALLY_AVAILABLE
            else:
                avail_status = AvailabilityStatus.UNAVAILABLE
            return AvailabilityResult(
                site=hut_name,
                status=avail_status,
                evidence=(
                    f"{count_source}={count!r} peopleCell={people_text!r} "
                    f"sitesCell={sites_text!r} icon={icon!r}"
                ),
                total_available=count,
                icon=icon,
            )

        return AvailabilityResult(
            site=hut_name,
            status=AvailabilityStatus.UNKNOWN,
            evidence=(
                f"Ambiguous state. peopleCell={people_text!r} "
                f"sitesCell={sites_text!r} siteCell={site_cell_text!r} "
                f"icon={icon!r}"
            ),
            icon=icon,
        )

