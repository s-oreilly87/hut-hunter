"""Adapter for the "standard" (non-Great-Walk) DOC booking flow.

The Great Walk booking site uses a single search form and a unified result
grid (handled by ``doc_great_walk.DocGreatWalkAdapter``). Standard huts,
campsites, lodges and Mueller Hut-style sites use a different booking flow
accessed via URLs of the form::

    https://bookings.doc.govt.nz/Web/#!park/{park_id}/{facility_id}

On that page:
  * The date range is edited via a custom date-range popup (clicking the
    "1 Night · Wed, Apr 01 - Thu, Apr 02" booking bar opens a two-month
    calendar where the user picks a start day, then an end day).
  * Availability is shown in a wide "Site List" table with a "Site"
    column, "Available # of People" row, "Number of sites available"
    row, and one column per day in the selected range.
"""

