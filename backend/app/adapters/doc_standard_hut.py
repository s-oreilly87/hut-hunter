@dataclass
class _DateParts:
    day: int
    month: str       # full month name, e.g. "April"
    year: int

    @classmethod
    def from_ddmmyyyy(cls, s: str) -> "_DateParts":
        dd, mm, yyyy = s.split("/")
        return cls(day=int(dd), month=MONTHS[int(mm) - 1], year=int(yyyy))


def _normalize(s: str | None) -> str:
    return re.sub(r"\s+", " ", s or "").strip()


def _parse_site_cell_status(site_cell_html: str) -> tuple[bool, str, str | None]:
    """Return ``(bookable, aria_status, icon)`` for a site-list cell button."""
    icon_match = re.search(
        r"(?:Keechma/icons|themes/[^/]+)/([^\"'()/]+)\.svg",
        site_cell_html, re.IGNORECASE,
    )
    icon = icon_match.group(1) if icon_match else None

    aria_label_match = re.search(
        r'aria-label=["\'][^"\']*-\s*([^"\']+)["\']', site_cell_html, re.IGNORECASE
    )
    aria_status = aria_label_match.group(1).strip().lower() if aria_label_match else ""

    cell_is_bookable = bool(re.search(r'tabindex=["\']0["\']', site_cell_html))
    if not cell_is_bookable and aria_status:
        cell_is_bookable = not re.search(
            r"booked|closed|unavailable|not.available|outside|locked|walk-?in",
            aria_status,
        )

    return cell_is_bookable, aria_status, icon


def _build_booking_bar_regexes(start: _DateParts, end: _DateParts) -> dict:
    """Regexes that match the booking-bar label once the date range is set.

    The bar is typically rendered as e.g. "1 Night Wed, Apr 01 - Thu, Apr 02"
    but the day number may or may not have a leading zero, and DOC has been
    observed using either the short ("Apr") or full ("April") month name.
    """
    s_m = MONTH_SHORT[start.month]
    e_m = MONTH_SHORT[end.month]

    s_month_group = f"({s_m}|{start.month})"
    e_month_group = f"({e_m}|{end.month})"

    start_pat = rf"{s_month_group}\s*0?{start.day}\b"
    end_pat = rf"{e_month_group}\s*0?{end.day}\b"

    range_pat = re.compile(rf"{start_pat}.*[\-\u2013].*{end_pat}", re.IGNORECASE)
    return {
        "range": range_pat,
        "start": re.compile(start_pat, re.IGNORECASE),
        "end": re.compile(end_pat, re.IGNORECASE),
    }


