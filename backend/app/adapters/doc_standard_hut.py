logger = logging.getLogger(__name__)


MONTH_SHORT = {m: m[:3] for m in MONTHS}


# NOTE: an earlier commit tried to bake DOC's listbox options into a
# constant here, but the listbox `id` attributes turn out to be opaque
# "place" ids — not park_id / facility_id — and therefore can't be used
# to build a booking URL. The real catalog lives in doc_standard_huts.json
# and is produced by backend/scripts/scrape_standard_huts.py, which walks
# the booking site end-to-end to capture the ``(park_id, facility_id)``
# pairs needed for ``!park/{park_id}/{facility_id}``.


# -------------------------------------------------------------------------- #
# Hut catalog — loaded from doc_standard_huts.json (generated by
# backend/scripts/scrape_standard_huts.py). Kept in-memory; cheap to
# reload if the file is updated at runtime.
# -------------------------------------------------------------------------- #

_HUT_CATALOG_PATH = Path(__file__).with_name("doc_standard_huts.json")

# The option string the frontend sees / sends back. Putting the hut name
# first makes alphabetic sort useful; embedding the IDs keeps the value
# self-describing so we don't need a separate lookup at submit time.
#   "Mueller Hut (747/2487) — Aoraki/Mount Cook National Park"
_FACILITY_OPTION_RE = re.compile(
    r"^(?P<hut>.+?)\s*\((?P<park>\d+)/(?P<facility>\d+)\)(?:\s*—\s*(?P<park_name>.+))?$"
)


def _format_facility_option(facility: dict, park_id: str, park_name: str) -> str:
    """Build the canonical option string for a single facility.

    Format: ``"Mueller Hut (747/2487) — Aoraki/Mount Cook National Park"``
    The embedded ``(park_id/facility_id)`` pair is what ``_FACILITY_OPTION_RE``
    unpacks at request time, so this format must not change without also
    updating that regex.
    """
    base = f"{facility['facility_name']} ({park_id}/{facility['facility_id']})"
    park = park_name.strip()
    return f"{base} — {park}" if park else base


def _parse_facility_option(value: str) -> tuple[str, str, str] | None:
    m = _FACILITY_OPTION_RE.match(value.strip())
    if not m:
        return None
    return m.group("park"), m.group("facility"), m.group("hut").strip()


def _load_hut_catalog() -> list[dict]:
    """Return the list of park dicts from doc_standard_huts.json.

    Each dict has the shape::

        {
          "park_id": "747",
          "park_name": "Aoraki/Mount Cook National Park",
          "facilities": [
            {"facility_id": "2487", "facility_name": "Mueller Hut"},
            ...
          ]
        }

    Parks are sorted alphabetically by park_name; facilities within each
    park are sorted alphabetically by facility_name.

    Returns an empty list (rather than raising) if the file is missing or
    malformed, so a broken catalog file doesn't prevent the rest of the
    adapter surface from loading.
    """
    try:
        raw = json.loads(_HUT_CATALOG_PATH.read_text())
    except FileNotFoundError:
        logger.warning("doc_standard_huts.json not found at %s — facility list empty", _HUT_CATALOG_PATH)
        return []
    except Exception as e:
        logger.error("failed to load doc_standard_huts.json: %s", e)
        return []

    parks = raw.get("parks") or []
    # Sort parks alphabetically; sort facilities within each park.
    parks = sorted(parks, key=lambda p: (p.get("park_name") or "").lower())
    for park in parks:
        park["facilities"] = sorted(
            park.get("facilities") or [],
            key=lambda f: (f.get("facility_name") or "").lower(),
        )
    return parks


def _facility_options_tree() -> list[dict]:
    """Build the grouped options tree for the frontend's <SelectGroup> rendering.

    Returns a list of ``{"group": park_name, "items": [opt_str, …]}`` dicts,
    one per park, in park_name alphabetical order.
    """
    result = []
    for park in _load_hut_catalog():
        park_id = park.get("park_id", "")
        park_name = park.get("park_name", "")
        items = [
            _format_facility_option(f, park_id, park_name)
            for f in park.get("facilities") or []
        ]
        if items:
            result.append({"group": park_name, "items": items})
    return result



