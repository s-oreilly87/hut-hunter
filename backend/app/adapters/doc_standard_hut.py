class DocStandardHutAdapter(BaseDOCAdapter):
    """Availability + hold adapter for the "regular" DOC booking flow.

    Facilities are selected from the scraped catalog (doc_standard_huts.json)
    via the ``facility`` param, which encodes the park ID, facility ID, and
    names in a single option string parsed by ``_parse_facility_option``.
    """

    adapter_id = "doc_standard_hut"
    name = "NZ DOC Standard Hut/Campsite"
    base_url = "https://bookings.doc.govt.nz/Web/#!park/{park_id}/{facility_id}"

    # ---------------------------------------------------------------- #
    # Param schema
    # ---------------------------------------------------------------- #

    @classmethod
    def param_fields(cls) -> list[ParamField]:
        options_tree = _facility_options_tree()

        return [
            ParamField(
                key="facility",
                label="Hut / Campsite",
                type="select",
                options_tree=options_tree,
                default="",
                required=True,
            ),
            ParamField(
                key="nights",
                label="Nights",
                type="number",
                default=1,
            ),
            ParamField(
                key="date",
                label="Start Date",
                type="date",
                required=True,
            ),
            ParamField(
                key="occupants",
                label="Occupants",
                type="text",
                default=(
                    '[{"first_name": "","last_name": "",'
                    '"category": "NZ Adult (18+)","country": "New Zealand",'
                    '"age": "","gender": "Male"}]'
                ),
                required=True,
            ),
            ParamField(
                key="people",
                label="People",
                type="select",
                options=PEOPLE_OPTIONS,
                default="2",
            ),
        ]

    # ---------------------------------------------------------------- #
    # Site resolution
    # ---------------------------------------------------------------- #

    def _resolve_site(self, params: dict) -> tuple[str, str, str]:
        """Return ``(park_id, facility_id, hut_name)`` from the ``facility``
        param string.  Raises ``ValueError`` if the string is missing or
        doesn't match the expected format.
        """
        facility = str(params.get("facility") or "").strip()
        if facility:
            parsed = _parse_facility_option(facility)
            if parsed is not None:
                return parsed

        raise ValueError(
            "DocStandardHutAdapter requires a valid `facility` option string"
        )

    # ---------------------------------------------------------------- #
    # URL helpers
    # ---------------------------------------------------------------- #

    def _url_for(self, params: dict) -> str:
        park_id, facility_id, _ = self._resolve_site(params)
        return self.base_url.format(park_id=park_id, facility_id=facility_id)

    def _date_range(self, params: dict) -> tuple[_DateParts, _DateParts]:
        start = _DateParts.from_ddmmyyyy(params["date"])
        nights = int(params.get("nights", 1))
        end_dt = datetime(start.year, MONTHS.index(start.month) + 1, start.day) \
            + timedelta(days=max(nights, 1))
        end = _DateParts(
            day=end_dt.day,
            month=MONTHS[end_dt.month - 1],
            year=end_dt.year,
        )
        return start, end

    # ---------------------------------------------------------------- #
    # Small Playwright helpers
    # ---------------------------------------------------------------- #

    async def _first_visible(self, loc: Locator) -> Locator:
        count = await loc.count()
        for i in range(count):
            item = loc.nth(i)
            try:
                if await item.is_visible():
                    return item
            except Exception:
                continue
        raise RuntimeError("No visible matching locator found")

    # ---------------------------------------------------------------- #
    # Booking-bar helpers (the "1 Night · Wed, Apr 01 - Thu, Apr 02" bar)
    # ---------------------------------------------------------------- #

    # Matches either a full "Mon, Apr 01 - Tue, Apr 02" range or half of one
    # plus "End Date" (which is what DOC shows when only the start has been
    # picked so far).
    _DATE_RANGE_TEXT_RE = re.compile(
        r"[A-Z][a-z]{2},\s*[A-Z][a-z]{2}\s*\d{2}\s*-\s*"
        r"([A-Z][a-z]{2},\s*)?[A-Z][a-z]{2}\s*\d{2}"
        r"|[A-Z][a-z]{2},\s*[A-Z][a-z]{2}\s*\d{2}\s*-\s*End Date"
    )
    _NIGHT_TEXT_RE = re.compile(r"^\d+\s*Night(s)?$", re.IGNORECASE)

    async def _get_date_range_locator(self, page: Page) -> Locator:
        return await self._first_visible(page.get_by_text(self._DATE_RANGE_TEXT_RE))

    async def _get_night_locator(self, page: Page) -> Locator:
        return await self._first_visible(page.get_by_text(self._NIGHT_TEXT_RE))

    async def _read_booking_bar_text(self, page: Page) -> str:
        """Return the full booking-bar string, normalised.

        The bar sits in a container with both "1 Night" and "Wed, Apr 01 -
        Thu, Apr 02" children. We anchor on the date-range text and climb
        one level up to capture the surrounding container.
        """
        date_loc = await self._get_date_range_locator(page)
        container = date_loc.locator("xpath=ancestor::div[1]")
        try:
            txt = await container.inner_text()
        except Exception:
            txt = await date_loc.inner_text()
        return _normalize(txt)

    # ---------------------------------------------------------------- #
    # Datepicker popup helpers
    # ---------------------------------------------------------------- #

    # Selector that matches the datepicker popup container when it's open.
    # The DOC booking SPA uses react-datepicker under the hood.
    _POPUP_SEL = ".react-datepicker-popper, .react-datepicker"

    async def _datepicker_popup(self, page: Page) -> Locator | None:
        """Return the visible datepicker popup locator, or None if closed."""
        for sel in self._POPUP_SEL.split(", "):
            loc = page.locator(sel.strip()).first
            try:
                if await loc.count() > 0 and await loc.is_visible():
                    return loc
            except Exception:
                continue
        return None

    async def _is_datepicker_open(self, page: Page) -> bool:
        """Return True if the calendar picker is currently visible.

        Uses the "Su" weekday-column header as the primary signal — it appears
        in the open calendar's day-name row and is not present elsewhere on the
        DOC booking page.  Falls back to CSS-class selectors as a secondary
        check.
        """
        # Primary: the "Su" column header only appears when the calendar is open
        try:
            su = page.get_by_text(re.compile(r"^Su$")).first
            if await su.count() > 0 and await su.is_visible():
                return True
        except Exception:
            pass
        # Secondary: react-datepicker CSS class selectors
        return (await self._datepicker_popup(page)) is not None

    async def _close_datepicker(self, page: Page) -> None:
        """Close the datepicker popup if it's open.

        Tries Escape first (cleanest), then a click on a neutral corner of the
        page body as a fallback.
        """
        if not await self._is_datepicker_open(page):
            return
        try:
            await page.keyboard.press("Escape")
            await page.wait_for_timeout(400)
        except Exception:
            pass
        if await self._is_datepicker_open(page):
            # Fallback: click a neutral spot (top-left corner of the viewport)
            try:
                await page.mouse.click(5, 5)
                await page.wait_for_timeout(400)
            except Exception:
                pass

    async def _open_datepicker(self, page: Page) -> None:
        """Open the date-range popup by clicking the booking bar.

        If the popup is already open (the site sometimes auto-opens it on page
        load), return immediately without toggling it closed.

        Tries clicking progressively broader ancestors of the date-range text
        until we see a known popup signal (month label, weekday header, etc.).
        """
        # Already open — nothing to do
        if await self._is_datepicker_open(page):
            logger.debug("Datepicker already open — skipping open click")
            return

        date_text = await self._get_date_range_locator(page)
        night_text = await self._get_night_locator(page)

        candidates: list[Locator] = [
            date_text,
            date_text.locator("xpath=ancestor::div[1]"),
            date_text.locator("xpath=ancestor::div[2]"),
            date_text.locator("xpath=ancestor::div[3]"),
            night_text,
            night_text.locator("xpath=ancestor::div[1]"),
            night_text.locator("xpath=ancestor::div[2]"),
        ]

        popup_signals = [
            page.get_by_text(re.compile(r"Furthest Arrival Date", re.IGNORECASE)).first,
            page.get_by_text(
                re.compile(
                    r"^(January|February|March|April|May|June|July|August"
                    r"|September|October|November|December)\s+\d{4}$"
                )
            ).first,
            page.get_by_text(re.compile(r"^Su$")).first,  # weekday header
        ]

        for i, target in enumerate(candidates):
            try:
                if await target.count() == 0:
                    continue
                try:
                    await target.scroll_into_view_if_needed()
                except Exception:
                    pass
                await target.click(force=True, timeout=4000)
            except Exception as e:
                logger.debug(f"openDatepicker candidate {i} click failed: {e}")
                continue

            # Poll for a popup signal for up to 4s
            for _ in range(27):
                if await self._is_datepicker_open(page):
                    return
                for sig in popup_signals:
                    try:
                        if await sig.count() > 0 and await sig.is_visible():
                            return
                    except Exception:
                        pass
                await page.wait_for_timeout(150)

        await self.snapshot(page, "datepicker_did_not_open")
        raise RuntimeError(
            "Could not open standard-hut datepicker by clicking the booking bar"
        )

    # ---------------------------------------------------------------- #
    # Date-range calendar popup
    # ---------------------------------------------------------------- #

    async def _visible_month_labels(self, scope: Page | Locator) -> list[str]:
        try:
            raw = await scope.get_by_text(
                re.compile(
                    r"^(January|February|March|April|May|June|July|August"
                    r"|September|October|November|December)\s+\d{4}$"
                )
            ).all_inner_texts()
        except Exception:
            return []
        return [_normalize(t) for t in raw if _normalize(t)]

    async def _popup_has_month(self, page: Page, month: str, year: int) -> bool:
        labels = await self._visible_month_labels(page)
        want = f"{month} {year}".lower()
        return any(lbl.lower() == want for lbl in labels)

    async def _click_month_nav(self, page: Page, direction: str) -> None:
        """Click the calendar's prev/next-month chevron.

        Scopes the button search to the datepicker popup so we don't
        accidentally click page-level buttons (accordions, breadcrumb arrows,
        share buttons, etc.) that happen to be left-most / right-most on the
        page.  Falls back to page-wide search only if the popup can't be
        identified.
        """
        popup = await self._datepicker_popup(page)
        scope: Page | Locator = popup if popup is not None else page

        buttons = scope.locator("button")
        count = await buttons.count()
        candidates: list[tuple[int, float]] = []  # (index, x-position)
        for i in range(count):
            b = buttons.nth(i)
            try:
                box = await b.bounding_box()
                if not box or not await b.is_visible():
                    continue
            except Exception:
                continue
            candidates.append((i, box["x"]))

        if candidates:
            if direction == "next":
                chosen_idx = max(candidates, key=lambda c: c[1])[0]
            else:
                chosen_idx = min(candidates, key=lambda c: c[1])[0]
            await buttons.nth(chosen_idx).click(force=True, timeout=5000)
            return

        # Fallback: aria-label or text-based selectors
        aria = "next" if direction == "next" else "previous"
        text_sel = ">" if direction == "next" else "<"
        for sel in [
            f'button[aria-label*="{aria}" i]',
            f'button:has-text("{text_sel}")',
        ]:
            loc = page.locator(sel).first
            if await loc.count() > 0:
                await loc.click(force=True, timeout=5000)
                return

        raise RuntimeError(f"Could not click {direction} month navigation")

    async def _ensure_month_visible(self, page: Page, month: str, year: int) -> None:
        for _ in range(24):
            if await self._popup_has_month(page, month, year):
                return
            await self._click_month_nav(page, "next")
            await page.wait_for_timeout(300)
        raise RuntimeError(f"Could not bring {month} {year} into calendar view")

    async def _click_day_in_visible_month(
        self, page: Page, month: str, year: int, day: int
    ) -> None:
        """Pick a day number inside the currently-visible ``month year`` block.

        Strategy (in order):
        1. Role-based: try button / option / gridcell with aria-label patterns.
        2. Ancestor-scoped text: find the month heading, walk up the DOM to its
           containing section (depths 1–5), then click the first element whose
           full text is exactly ``{day}``.  This is scoped to the right calendar
           panel so it can't hit day numbers in the booking bar or other panels.
        3. JS fallback: use ``page.evaluate`` to walk the live DOM, find the
           month heading by innerText, and click the first leaf element whose
           text is the day number — works regardless of CSS class names.
        """
        day_re = re.compile(rf"^{day}$")
        day_str = str(day)
        month_year = f"{month} {year}"

        # ── 1. Role-based aria-label search ──────────────────────────────
        aria_patterns = [
            re.compile(rf"^{month}\s+{day},\s+{year}$", re.IGNORECASE),
            re.compile(rf"\b{month}\s+{day}\b", re.IGNORECASE),
            day_re,
        ]
        for pat in aria_patterns:
            for role in ("button", "option", "gridcell"):
                el = page.get_by_role(role, name=pat).first  # type: ignore[arg-type]
                try:
                    if await el.count() > 0:
                        await el.click(force=True, timeout=4000)
                        return
                except Exception:
                    continue

        # ── 2. Ancestor-scoped text search ────────────────────────────────
        month_label = page.get_by_text(
            re.compile(rf"^{month}\s+{year}$", re.IGNORECASE)
        ).first
        if await month_label.count() > 0:
            for depth in range(1, 6):
                container = month_label.locator(f"xpath=ancestor::div[{depth}]")
                try:
                    if await container.count() == 0:
                        continue
                    # Try role-based first within the scoped container
                    for role in ("button", "option", "gridcell"):
                        day_el = container.get_by_role(role, name=day_re).first  # type: ignore[arg-type]
                        if await day_el.count() > 0:
                            await day_el.click(force=True, timeout=4000)
                            return
                    # Try any element whose FULL text is the day number
                    day_el = container.get_by_text(day_re).first
                    if await day_el.count() > 0:
                        await day_el.click(force=True, timeout=4000)
                        return
                except Exception:
                    continue

        # ── 3. JS fallback — walk the live DOM ───────────────────────────
        # Scope to the section containing the month heading so we don't
        # accidentally click a day number that appears elsewhere on the page
        # (e.g. the booking bar "Apr 22" would otherwise match day 22).
        clicked = await page.evaluate(f"""
            (function() {{
                // Find the element whose visible text is exactly the month+year
                const allEls = Array.from(document.querySelectorAll('*'));
                const monthEl = allEls.find(el =>
                    (el.innerText || '').trim() === {json.dumps(month_year)}
                );
                if (!monthEl) return false;

                // Walk up to find a container that also holds the day grid
                let container = monthEl;
                for (let i = 0; i < 6; i++) {{
                    container = container.parentElement;
                    if (!container) break;

                    // Find leaf elements (no child elements) with the exact day number
                    const leaves = Array.from(container.querySelectorAll('*')).filter(el =>
                        el.childElementCount === 0 &&
                        (el.innerText || el.textContent || '').trim() === {json.dumps(day_str)}
                    );
                    if (leaves.length > 0) {{
                        leaves[0].click();
                        return true;
                    }}
                }}
                return false;
            }})()
        """)
        if clicked:
            return

        raise RuntimeError(f"Could not click day {day} in {month} {year}")

    async def _set_date_range(
        self, page: Page, start: _DateParts, end: _DateParts
    ) -> None:
        await self._open_datepicker(page)

        await self._ensure_month_visible(page, start.month, start.year)
        await self._click_day_in_visible_month(page, start.month, start.year, start.day)
        await page.wait_for_timeout(400)

        await self._ensure_month_visible(page, end.month, end.year)
        await self._click_day_in_visible_month(page, end.month, end.year, end.day)
        await page.wait_for_timeout(400)

        # The popup usually closes itself once the end date is selected.
        # If it's still open after 3s, force-close it — otherwise
        # _wait_for_booking_bar_dates will read a stale / partial bar.
        closed = False
        for _ in range(15):
            if not await self._is_datepicker_open(page):
                closed = True
                break
            await page.wait_for_timeout(200)
        if not closed:
            logger.debug("Datepicker still open after end-date click — force-closing")
            await self._close_datepicker(page)
            await page.wait_for_timeout(300)

    async def _wait_for_booking_bar_dates(
        self, page: Page, start: _DateParts, end: _DateParts, timeout_ms: int = 20_000
    ) -> None:
        # Ensure the datepicker is closed before reading the bar — if it's open
        # the bar text may not have settled yet, and _get_date_range_locator can
        # find date strings inside the popup instead of the bar itself.
        await self._close_datepicker(page)
        await page.wait_for_timeout(400)

        regexes = _build_booking_bar_regexes(start, end)
        elapsed = 0
        last = ""
        while elapsed < timeout_ms:
            last = await self._read_booking_bar_text(page)
            if regexes["range"].search(last) or (
                regexes["start"].search(last) and regexes["end"].search(last)
            ):
                # Let the Site List table refresh in response.
                await page.wait_for_timeout(800)
                return
            await page.wait_for_timeout(250)
            elapsed += 250

        await self.snapshot(page, "booking_bar_did_not_update")
        raise TimeoutError(
            f"Booking bar did not update to target range. Last saw: {last!r}"
        )

