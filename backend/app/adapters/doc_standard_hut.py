    # attempt_hold — standard flow (best-effort port)
    # ---------------------------------------------------------------- #

    async def attempt_hold(self, page: Page, params: dict) -> BookingResult:
        """Drive the "regular" DOC hold flow.

        The non-Great-Walk booking flow differs from the Great Walk one:
        instead of clicking a cell in the ``table.js-book-modal`` grid, the
        user clicks the site column in the Site List to reveal a per-site
        booking panel, sets a quantity, presses an "Add to Booking" / "Book
        Now" button, then continues through login → occupant details →
        Shopping Cart → CreditCardPayment.

        The selectors for the per-site panel are not documented in the
        watch.ts script (it only did availability detection), so this
        implementation anchors on visible text ("Book Now", "Add to
        Booking", "Continue", "Reserve") and falls back to role-based
        matching. Once the flow reaches ``CreditCardPayment`` we reuse the
        Great Walk adapter's cart-capture logic verbatim. If any step
        fails, we snapshot the page and return a non-held BookingResult so
        the caller notifies the user.
        """
        try:
            _park_id, _facility_id, hut_name = self._resolve_site(params)
        except ValueError as e:
            return BookingResult(success=False, held=False, message=str(e))

        people_wanted = int(params.get("people", 2))
        occupants = params.get("occupants", [])
        job_id_for_cart = params.get("_job_id", "unknown")
        start, _end = self._date_range(params)

        if not occupants:
            return BookingResult(
                success=False,
                held=False,
                message="No occupant details provided — cannot complete hold",
            )

        # --- 1. Click the site row button for the target date ----------- #
        # DOC renders each date cell as a <button> with an aria-label of the
        # form "{Site Name} MM/DD/YYYY - available".  Targeting by aria-label
        # is far more reliable than computing row/column indices through a
        # flex-layout table that can't be navigated with nth().
        month_num = MONTHS.index(start.month) + 1
        target_date_str = f"{month_num:02d}/{start.day:02d}/{start.year}"

        # Build candidates in order of specificity:
        #   1. Exact site-name + date match on a bookable cell
        #   2. Any bookable cell for the target date (campsite unit grids)
        #   3. Non-bookable fallbacks for diagnostics only
        site_btn = page.locator(
            f'button[aria-label*="{hut_name}"][aria-label*="{target_date_str}"][tabindex="0"]'
        )
        if await site_btn.count() == 0:
            site_btn = page.locator(
                f'button[aria-label*="{target_date_str}"][tabindex="0"]'
            ).filter(has=page.locator("img"))

        if await site_btn.count() == 0:
            site_btn = page.locator(
                f'button[aria-label*="{hut_name}"][aria-label*="{target_date_str}"]'
            )

        if await site_btn.count() == 0:
            # Fallback: any button whose aria-label contains the date and
            # contains an <img> (icon), excluding the datepicker buttons which
            # have no img child.
            site_btn = page.locator(
                f'button[aria-label*="{target_date_str}"]'
            ).filter(has=page.locator("img"))

        if await site_btn.count() == 0:
            await self.snapshot(page, "hold_no_site_button")
            return BookingResult(
                success=False,
                held=False,
                message=(
                    f"Could not find site button for {hut_name!r} on "
                    f"{target_date_str} — no matching aria-label"
                ),
            )

        btn = site_btn.first
        try:
            await btn.scroll_into_view_if_needed(timeout=10_000)
            await btn.evaluate("el => el.click()")
            logger.info(
                "Selected availability cell for %s on %s (aria-label targeting)",
                hut_name, target_date_str,
            )
        except Exception as e:
            await self.snapshot(page, "hold_cell_click_failed")
            return BookingResult(
                success=False,
                held=False,
                message=f"Could not click availability cell: {e}",
            )

        await page.wait_for_timeout(600)

        # --- 2. Click "Book Now" to proceed from the site list -------- #
        # For campsite-style facilities, clicking the date cell SELECTS it
        # (the cell turns into "Booking Selected") but the page stays on the
        # Site List.  The user must then click "Book Now" at the bottom of
        # the table to proceed.  Hut-style facilities may skip this step and
        # navigate directly to a booking form — handled below.
        _BOOK_NOW_RE = re.compile(
            r"^(Book Now|Add to Booking|Add to Cart)$", re.IGNORECASE
        )
        book_now_btn = page.get_by_role("button", name=_BOOK_NOW_RE).first
        try:
            await book_now_btn.wait_for(state="visible", timeout=6_000)
            await book_now_btn.click()
            logger.info("Clicked Book Now to proceed from site list")
            await page.wait_for_timeout(600)
        except PlaywrightTimeoutError:
            logger.info("No Book Now button found — assuming direct navigation after cell click")

        # --- 3. Handle login if prompted ------------------------------ #
        # Clicking "Book Now" (or the cell itself for huts) will trigger the
        # login modal when the user isn't authenticated.  After a successful
        # login, DOC stays on the current page — no re-click needed since the
        # site remembers the selection.
        try:
            await self._login_if_prompted(page)
            await page.wait_for_timeout(600)
        except RuntimeError as e:
            await self.snapshot(page, "login_error")
            return BookingResult(success=False, held=False, message=str(e))

        # --- 4. Detect which booking form appeared -------------------- #
        # Pattern A — Campsite "Reservation Details" inline form:
        #   After login (or if already logged in), DOC shows a page with a
        #   "Reserve Unit" button, adult-count select, and occupant name input.
        #
        # Pattern B — Hut / Great Walk occupant modal:
        #   An "Occupant Details" modal appears with per-person name/age fields.

        _RESERVE_UNIT_RE = re.compile(r"Reserve\s+Unit", re.IGNORECASE)
        reserve_unit_btn = page.get_by_role("button", name=_RESERVE_UNIT_RE)
        try:
            await reserve_unit_btn.wait_for(state="visible", timeout=15_000)
            is_campsite_form = True
            logger.info("Detected Reservation Details page (Reserve Unit button)")
        except PlaywrightTimeoutError:
            is_campsite_form = False

        if is_campsite_form:
            # ── Pattern A: fill the inline Reservation Details page ── #

            # 4a. Set adult count
            # DOC's adult-count select has aria-label="Adult (18+)" directly on
            # the <select> element (not a wrapping label).  Use that attribute to
            # target it precisely — other selects (Nights, Child, Infant) have
            # different aria-labels.
            try:
                adult_select = page.locator('select[aria-label*="Adult" i]').first
                if await adult_select.count() == 0:
                    # Fallback: select whose id ends in ddlPersonClassification_0
                    adult_select = page.locator(
                        'select[id$="ddlPersonClassification_0"]'
                    ).first
                await adult_select.select_option(str(people_wanted))
                logger.info("Set adult count to %d", people_wanted)
                await page.wait_for_timeout(400)
            except Exception as e:
                logger.warning("Could not set adult count: %s", e)

            # 4b. Fill "Occupant Name" (required — must be ≥ 3 chars)
            # DOC uses id="mainContent_txtOccupant" / placeholder="Enter occupant name".
            occ_name = ""
            if occupants:
                first = occupants[0].get("first_name", "")
                last = occupants[0].get("last_name", "")
                occ_name = f"{first} {last}".strip() or first or last
            if not occ_name:
                occ_name = "Guest"
            try:
                name_input = page.locator("#mainContent_txtOccupant").first
                if await name_input.count() == 0:
                    name_input = page.locator('[placeholder="Enter occupant name"]').first
                if await name_input.count() == 0:
                    name_input = page.locator('[aria-label="Occupant Name"]').first
                await name_input.wait_for(state="visible", timeout=5_000)
                await name_input.fill(occ_name)
                logger.info("Filled occupant name: %r", occ_name)
            except Exception as e:
                logger.warning("Could not fill occupant name: %s", e)

            # 4c. Accept T&C checkbox
            try:
                tc_checkbox = page.get_by_label(
                    re.compile(r"agree.*terms|terms.*conditions", re.IGNORECASE)
                ).first
                if await tc_checkbox.count() == 0:
                    tc_checkbox = page.locator('input[type="checkbox"]').first
                if not await tc_checkbox.is_checked():
                    await tc_checkbox.check()
                    logger.info("Checked T&C checkbox")
            except Exception as e:
                logger.warning("Could not check T&C checkbox: %s", e)

            await self.snapshot(page, "reservation_details")

            # 4d. Click "Reserve Unit"
            try:
                await reserve_unit_btn.click()
                logger.info("Clicked Reserve Unit")
                # DOC shows a "Message" validation dialog if required fields are
                # missing (e.g. "Occupant Name - must be at least 3 characters").
                # Detect it and surface a clear error rather than timing out later.
                await page.wait_for_timeout(800)
                msg_dialog = page.locator(".modal, [role='dialog']").filter(
                    has_text=re.compile(r"Fill in the following|must be at least", re.IGNORECASE)
                ).first
                if await msg_dialog.count() > 0 and await msg_dialog.is_visible():
                    msg_text = await msg_dialog.inner_text()
                    logger.warning("Reservation validation error: %s", msg_text.strip())
                    await self.snapshot(page, "reserve_unit_validation_error")
                    # Try to close the dialog
                    close_btn = msg_dialog.locator("button").first
                    if await close_btn.count() > 0:
                        await close_btn.click()
                    return BookingResult(
                        success=False, held=False,
                        message=f"Reservation form validation error: {msg_text.strip()[:200]}",
                    )
            except Exception as e:
                await self.snapshot(page, "reserve_unit_click_failed")
                return BookingResult(
                    success=False, held=False,
                    message=f"Could not click Reserve Unit: {e}",
                )

        else:
            # ── Pattern B: Occupant Details modal (hut-style) ──────── #
            try:
                await page.locator("text=Occupant Details").first.wait_for(
                    state="visible", timeout=10_000
                )
                logger.info("Occupant details modal appeared")
            except PlaywrightTimeoutError:
                await self.snapshot(page, "occupant_modal_timeout")
                return BookingResult(
                    success=False, held=False,
                    message="Neither Reserve Unit page nor Occupant Details modal appeared",
                )

            await self._fill_occupants(page, occupants)

            await page.get_by_role("button", name="Save & Continue").click()
            logger.info("Clicked Save & Continue")

        # --- 6. Intermediate Reservation Details page (hut/Great Walk only) -- #
        # For campsite Pattern A, clicking "Reserve Unit" navigates directly to
        # the Shopping Cart — there is no intermediate SelectReservationPreCart
        # step.  Only run this block for Pattern B (hut/Great Walk modal flow).
        if not is_campsite_form:
            try:
                await page.wait_for_url(
                    re.compile(r"SelectReservationPreCart", re.IGNORECASE),
                    timeout=15_000,
                )
                logger.info("Reached Reservation Details page")
            except PlaywrightTimeoutError:
                await self.snapshot(page, "reservation_details_url_timeout")
                return BookingResult(
                    success=False,
                    held=False,
                    message="Did not reach Reservation Details page",
                )

            try:
                book_link = page.locator("#mainContent_bReserve")
                await book_link.wait_for(state="visible", timeout=15_000)

                view_occ_btn = page.locator("#aViewOccupant")
                if await view_occ_btn.count() > 0:
                    await view_occ_btn.click()
                    logger.info("Opened View Occupants modal")
                    await page.locator("#myModal_occu").wait_for(
                        state="visible", timeout=5_000
                    )
                    await page.wait_for_timeout(300)
                    await self.snapshot(page, "reservation_details")
                    await page.locator("#myModal_occu .close").first.click()
                    await page.locator("#myModal_occu").wait_for(
                        state="hidden", timeout=5_000
                    )
                else:
                    await self.snapshot(page, "reservation_details")

                await book_link.click()
                logger.info("Clicked reserve (#mainContent_bReserve)")
            except PlaywrightTimeoutError:
                await self.snapshot(page, "reserve_link_timeout")
                return BookingResult(
                    success=False,
                    held=False,
                    message="Reserve link not found on Reservation Details page",
                )

        try:
            await page.wait_for_url("**/ShoppingCart**", timeout=15_000)
            logger.info("Reached Shopping Cart")
        except PlaywrightTimeoutError:
            await self.snapshot(page, "shopping_cart_timeout")
            return BookingResult(
                success=False,
                held=False,
                message="Did not reach Shopping Cart page",
            )

        await self._check_terms(page)

        cart_url = await self._proceed_to_checkout(page)
        if cart_url is None:
            return BookingResult(
                success=False, held=False,
                message="Did not reach payment page",
            )

        resume_url = await self._persist_cart_session(page, job_id_for_cart, cart_url)
        return BookingResult(
            success=True,
            held=True,
            reservation_url=resume_url,
            message=(
                f"Cart secured for {hut_name} on {params.get('date')}. "
                f"24 minutes to complete payment."
            ),
        )
